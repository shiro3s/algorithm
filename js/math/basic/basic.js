export const add = (a, b) => {
	return a + b;
};

export const sub = (a, b) => {
	return a - b;
};

export const mul = (a, b) => {
	return a * b;
};

export const div = (a, b) => {
	return a / b;
};

export const mod = (a, b) => {
	return a % b;
};

export const abs = (x) => {
	return Math.abs(x);
};

export const pow = (base, ex) => {
	return base ** ex;
};

export const sqrt = (x) => {
	return Math.sqrt(x);
};
